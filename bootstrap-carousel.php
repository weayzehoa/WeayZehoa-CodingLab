                <div class="col-10 offset-1">
                    <h3 >選項</h3>
                    <p>可以透過資料屬性或 JavaScript 調整選項。對於資料屬性，將選項名稱附加到 <code class="highlighter-rouge">data-</code>，如 <code class="highlighter-rouge">data-interval=""</code>。</p>
                    <table class="table table-bordered table-striped">
                    <thead>
                        <tr>
                        <th style="width: 100px;">名稱</th>
                        <th style="width: 50px;">種類</th>
                        <th style="width: 50px;">預設</th>
                        <th>描述</th>
                        </tr>
                    </thead>
                    <tbody>
                        <tr>
                        <td>interval</td>
                        <td>數值</td>
                        <td>5000</td>
                        <td>一個項目自動迴圈之間延遲的時間。 如果為 false ，輪播不會自動重播。</td>
                        </tr>
                        <tr>
                        <td>keyboard</td>
                        <td>boolean</td>
                        <td>true</td>
                        <td>輪播是否對於鍵盤事件有反應</td>
                        </tr>
                        <tr>
                        <td>pause</td>
                        <td>string | boolean</td>
                        <td>"hover"</td>
                        <td>
                            <p>
                            如果設置為 <code>"hover"</code>, 輪播會因為 <code>mouseenter</code> 而暫停並在 <code>mouseleave</code> 時再度執行。如果設置 <code>false</code>，滑入時將不會停止輪播。
                            </p>
                            <p>在觸控裝置上 <code>"hover"</code>。在重播恢復之前，輪播將停止於 <code>touchend</code> (直到用戶與輪播的互動) 兩次之間的期間。注意，這是除了滑鼠行為以外。</p>
                        </td>
                        </tr>
                        <tr>
                        <td>ride</td>
                        <td>string</td>
                        <td>false</td>
                        <td>
                            用戶手動播放第一個後自動輪播。如果是 "carousel" 在輪播載入後自動播放。
                        </td>
                        </tr>
                        <tr>
                        <td>wrap</td>
                        <td>boolean</td>
                        <td>true</td>
                        <td>輪播是否應該連續循環或停止。</td>
                        </tr>
                    </tbody>
                    </table>
